// ARITHMETIC OPERATOR
#include <stdio.h>
#include <conio.h>

int main() {
    int a = 20, b = 10;

    printf("%d\n", a + b); // Addition
    printf("%d\n", a - b); // Subtraction
    printf("%d\n", a * b); // Multiplication
    printf("%d\n", a / b); // Division
    printf("%d\n", a % b); // Modulus

    return 0;
}

